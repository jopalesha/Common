using System;

namespace Jopalesha.Common.Domain
{
    public class Key<T> : IKey<T>
    {
        public Key(T value) : this()
        {
            Value = value ?? throw new ArgumentNullException(nameof(value));
        }

        protected Key() { }

        public static Key<T> Generated => new AutogeneratedKey<T>();

        public T Value { get; }
    }
}